// 0 até 9, de 1 em 1
for (let index = 0; index < 10; index++) {
    console.log(index);
}

console.log('')

// 1 até 10, de 2 em 2
for (let index = 1; index < 11; index+=2) {
    console.log(index);
}

console.log('')

// 10 até 1, de 1 em 1
for (let index = 10; index > 0; index--) {
    console.log(index);
}

console.log('')

// 10 até 1, de 2 em 2
for (let index = 10; index > 0; index-=2) {
    console.log(index);
}